# Planck constant (m^2 kg / s)
h = 6.62607015e-34
"""Plank constant."""

# speed of light (m / s)
c = 2.99792458e8
"""Speed of light."""

# elementary charge (C)
e = 1.60217663e-19
"""Elementary charge."""

# composite constants
hc = h * c
"""Product of Plank constant and speed of light."""

magnetic_flux_quantum = h / e
"""Magnetic flux quantum."""

klitzing_constant = h / e**2
"""Klitzing constant."""
